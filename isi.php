            <div class="four">Kisah Nabi Ayyub : Belajar Sabar Menghadapi Ujian</div>
            <div class="kisah">
                Sebelum ujian kesabaran menimpanya, Nabi Ayyub diberikan limpahan karunia nikmat oleh Allah.
                Beliau dikaruniai badan sehat dengan wajah yang rupawan. Di sisi materi, Allah pun memberikan harta yang melimpah 
                sehingga Nabi Ayyub menjadi seorang yang kaya raya. Tak hanya berupa uang, harta tersebut juga mewujud dalam bentuk tanah dan bangunan yang luas.
                Dengan banyaknya nikmat yang telah Allah diberikan, Nabi Ayyub pun rajin bersyukur. Beliau menjadi seorang hamba yang bertakwa dan menyayangi sesama. 
                <br><br>
                Allah memberi ujian kepada Nabi Ayyub dengan mengambil kembali seluruh nikmat berlimpah yang Dia berikan kepada beliau. 
                Pertama, Allah timpakan kepada Nabi Ayyub penyakit kulit di sekujur tubuhnya.
                Bahkan Ibnu Katsir menafsirkan, tidak ada satu pori-pori pun dari tubuh Nabi Ayyub yang selamat dari penyakit judzam (kusta) itu. 
                Dengan itu Allah mengangkat nikmat paras beliau yang rupawan. <br>
                Badannya yang semula sehat, segar, dan bugar Allah angkat dengan menimpakan tubuh yang sangat lemah karena penyakit itu.
                Putra-putri beliau pun Allah ambil, semuanya meninggal dunia. Nabi Ayyub pun jatuh miskin. Ditambah dengan kondisi penyakitnya, semua orang menjauhi beliau. 
                Nabi Ayyub pun mengasingkan diri ke suatu tempat. 
                Hanya istri beliau yang setia menemani, juga dua orang sahabat beliau yang selalu mengunjungi.
                <br> <br>
                Hingga pada suatu hari saat membawakan makanan untuk Nabi Ayyub, sang istri mengusulkan sesuatu kepada beliau.
                “Wahai Ayyub, seandainya engkau mau meminta kepada Allah, tentu Dia akan memberimu jalan keluar,” demikian saran istrinya yang tak tega melihat Nabi Ayyub sakit demikian lama.
                Namun, apa jawab Nabi Ayyub? Beliau menolak dengan alasan yang luar biasa.
                <br> <br>
                “Aku telah diberi kesehatan selama 70 tahun. Sakit ini masih derita sedikit yang Allah timpakan sampai aku bisa bersabar sama seperti masa sehatku yaitu 70 tahun,” demikian jawaban Nabi Ayyub.
                Jawaban tersebut juga sekaligus menjadi penegas bahwa hati beliau sama sekali tidak terpengaruh dengan ujian dahsyat yang ditimpakan Allah.
                <br><br>
                Karena kesabaran Nabi Ayyub yang luar biasa saat menjalani ujian tersebut, Allah pun menunjukkan jalan keluar.
                Allah mewahyukan kepada Nabi Ayyub untuk menghentakkan kaki beliau ke tanah. Kemudian dari sana Allah pancarkan mata air yang sejuk. 
                Nabi Ayyub pun bergegas mandi di mata air tersebut hingga sembuhlah penyakit kulit dari badan beliau.
